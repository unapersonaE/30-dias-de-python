# Script that prompts the user to enter base and height of the triangle and calculate an area of this triangle
# Stores the user input as an integer in the 'base' variable
base = int(input("What's the base of the triangle?"))
# Stores the user input as an integer in the 'base' variable
altura = int(input("What's the height of the triangle?"))
# Calculating area of a triangle
area_of_triangle = (base * altura)/2
print('Area of triangle:', area_of_triangle)